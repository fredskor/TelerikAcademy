using System;

class DeclareVariables
{
    static void Main()
    {
        
        ushort valueOne = 52130;
        sbyte valueTwo = -115;
        int valueThree = 4825932;
        byte valueFour = 97;
        short valueFive = -10000;
       
        Console.WriteLine(("ushort --> ") + valueOne);
        Console.WriteLine(("sbyte --> ") + valueTwo);
        Console.WriteLine(("int --> ") + valueThree);
        Console.WriteLine(("byte --> ") + valueFour);
        Console.WriteLine(("short --> ") + valueFive);
    }
}